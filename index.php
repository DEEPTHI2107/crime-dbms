Report a crime online<br>

If you or someone you know has been a victim of a crime in KARNATAKA, you can report it to KARNATAKA Police online.
<br>
<h4>When not to use this form</h4>
In an emergency, always call 100. For example, you should always call 100 and never use the online crime reporting tool if:
<br>
*The crime is happening now<br>
*Offenders are still nearby<br>
*You or someone else is in danger<br>
*Someone is injured<br>
*Immediate action is required<br>
*You are wanting to report a stolen vehicle<br>
*If you are wanting to report a problem with antisocial behaviour, such as ‘rowdy’ behaviour, street drinking, setting off fireworks late at night or problems with noise.<br>
*If you are reporting a crime or incident involing a current or ex-partner<br>
*If you are reporting a sexual offence<br>

<h4>When you can use this form</h4>
Online crime reporting has been introduced to offer another option to victims of crime when reporting a crime or incident where an urgent police response is not required. Examples of crimes and incidents you can report online include:

*Criminal damage and vandalism
*Thefts from a vehicle
*Thefts of a mobile phone
*Shed, garage and outbuilding burglaries
<br>
How we will deal with your report
Once we have received your report, we will aim to deal with it within 24 hours. 
you can contact us at any time and track your complaint.

Any personal information submitted as part of your report will only be held for policing purposes, in accordance with the Data Protection Act 1998.
